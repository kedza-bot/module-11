#Operators

#Arithmetic Operators

# Addition (+)
# Subtraction (-)
# Multiplication (*)
# Division (/)
# Modulus (%)
# Exponentiation (**)
# Floor Division (//)

X = 10
Y = 3

# Addition
print(X + Y)  

# Subtraction
print(X - Y)

# Multiplication

print(X * Y)

# Division
print(X / Y)

# Modulus
print(X % Y)  # Remainder of the division

# Exponentiation
print(X ** Y)  # X raised to the power of Y

# Floor Division

print(X // Y)  # Division that rounds down to the nearest integer

#aguements  make it easier to read and understand the code. short-hand operators
# Short-hand Operators

b = 10
b **= 2 # Equivalent to b = b + 2
print(b) 
